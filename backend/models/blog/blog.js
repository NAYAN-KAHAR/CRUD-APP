
import mongoose from 'mongoose';

// Create a new mongoose.Schema
const blogSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: {
      createdAt: 'created_at',
      updatedAt: 'updated_at',
    },
  }
);

// Create a model
const Blog = mongoose.model('Blog', blogSchema);

export default Blog;

